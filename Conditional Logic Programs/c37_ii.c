//Vowel or Consonant using switch case

#include<stdio.h>
int main()
{
	char ch;
	printf("Enter The Character:");
	scanf(" %c",&ch);
	switch(ch)
	{
		case 'a':
			
		case 'e':
			
		case 'i':
			
		case 'o':
			
		case 'u':
			
		case 'A':
			
		case 'E':
			
		case 'I':
			
		case 'O':
	
		case 'U':
			
		printf("%c Is Vowel",ch);
		break;
	    default:
				printf("%c Is consonant",ch);
	}
	return 0;
}
